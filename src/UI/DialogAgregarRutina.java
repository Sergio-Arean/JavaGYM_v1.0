    private void CargarNombresInstructores(){
        ArrayList <String> nombres_instructores =  ObtenerNombresInstructores();
        for(String nombre_instructor : nombres_instructores){
            cbNombresInstructores.addItem(nombre_instructor);
        }

    }
     private ArrayList<String> ObtenerNombresInstructores(){
        String json_instructores= gimnasio.CompartirDatosInstructores();
        ArrayList <String> nombres_instructores = new ArrayList<>();
        try {
            JSONArray jsonArray_clientes = new JSONArray(json_instructores);
            for(int i=0; i<jsonArray_clientes.length();i++){
                Instructor instructor = new Instructor();
                instructor = instructor.fromJson(jsonArray_clientes.getJSONObject(i));
                nombres_instructores.add(instructor.getNombre());
            }}catch(JSONException e){
                    Utilidades.MostrarMensajeInformativo(e.getMessage());
                    }
        return nombres_instructores;
        
    }

    private void CargarActividadesRealizadas(){
       String texto_actividades_realizadas = "Actividades Realizadas por Cliente:\n";
     
    try{
            JSONObject jSONObject_cliente = new JSONObject(json_cliente_seleccionado);
            JSONArray jSONArray_actividades = jSONObject_cliente.getJSONArray("Actividades");
            for(int i=0; i<jSONArray_actividades.length(); i++){
                JSONObject jSONObject_actividad = new JSONObject();
                jSONObject_actividad = jSONArray_actividades.getJSONObject(i);
                
                Actividad actividad = new Actividad();
                actividad = actividad.fromJson(jSONObject_actividad);
                texto_actividades_realizadas += actividad.getNombre().name() + "\n";
                
                
            }
            taActividadesRealizadas.setText(texto_actividades_realizadas);
        }catch(JSONException e){
            Utilidades.MostrarMensajeWarning("Hubo un problema al recuperar datos del cliente");
        }


    }


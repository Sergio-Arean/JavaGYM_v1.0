    private void SetearCampos(){
        //metodo para mostrar campos modificables en pantalla
        tfDni.setText(instructor_a_modificar.getDni());//inmodificable
        tfNombre.setText(instructor_a_modificar.getNombre());
        
        cbGenero.setSelectedItem(instructor_a_modificar.getGenero());
        
        tfDomicilio.setText(instructor_a_modificar.getDomicilio());
        
        //pasamos de localdate a date?
        LocalDate localdate_fecha_nacimiento = instructor_a_modificar.getFecha_nacimiento();
        ZoneId zoneId = ZoneId.systemDefault();
        Date date_fecha_nacimiento = Date.from(localdate_fecha_nacimiento.atStartOfDay(zoneId).toInstant());
        dcFechaNacimiento.setDate(date_fecha_nacimiento);
        
        tfTelefono.setText(instructor_a_modificar.getTelefono());
        tfTelEmergencia.setText(instructor_a_modificar.getContacto_emergencia());
        tfEmail.setText(instructor_a_modificar.getEmail());
        tfObraSocial.setText(instructor_a_modificar.getObra_social());
        
        cbGrupoSanguineo.setSelectedItem(instructor_a_modificar.getGrupo_sanguineo());
        
        tfCuil.setText(instructor_a_modificar.getCUIL());
        
    }



    private void ModificarInstructor() {
        //metodo invocado al pulsar boton de modificar (instructor)
        //Fecha de Nacimiento
        Date fecha_date = dcFechaNacimiento.getDate();
        Instant instant = fecha_date.toInstant();
        ZoneId zoneId = ZoneId.systemDefault();
        LocalDate fecha_de_nacimiento = instant.atZone(zoneId).toLocalDate();

        //Grupo Sanguineo
        String enum_formato_string = (String) cbGrupoSanguineo.getSelectedItem();
        EGrupoSanguineo grupo_sanguineo = EGrupoSanguineo.valueOf(enum_formato_string);

        //Genero
        String genero_formato_string = (String) cbGenero.getSelectedItem();
        EGenero genero = EGenero.valueOf(genero_formato_string);

        try {

            gimnasio.modificarInstructor(tfDni.getText(), tfNombre.getText(), tfDni.getText(), genero,
                    tfTelefono.getText(), tfDomicilio.getText(), tfEmail.getText(), instructor_a_modificar.getEstado(), grupo_sanguineo,
                    tfTelEmergencia.getText(), tfObraSocial.getText(), fecha_de_nacimiento, instructor_a_modificar.getComentario(),
                    tfCuil.getText(), instructor_a_modificar.getImagenPerfil());
            Utilidades.MostrarMensajeInformativo("El instructor ha sido modificado exitosamente!");
            this.dispose();

        } catch (NoEncontradoException e) {
            Utilidades.MostrarMensajeWarning(e.getMessage());
        }
        
    }
    private boolean ValidarCamposCompletos() {
        boolean rta = false;
        if(CampoInvalido(tfDni.getText()) || CampoInvalido(tfNombre.getText()) || CampoInvalido(tfCuil.getText()) || CampoInvalido(tfDomicilio.getText()) || CampoInvalido(tfTelefono.getText()) || CampoInvalido(tfTelEmergencia.getText())
                || CampoInvalido(tfEmail.getText()) || CampoInvalido(tfObraSocial.getText()) || cbGenero.getSelectedIndex()<0 || cbGrupoSanguineo.getSelectedIndex()<0 || dcFechaNacimiento.getDate()==null){
            Utilidades.MostrarMensajeWarning("Todos los campos deben estar completos!");
        }else{
            rta = true;
        }
        return rta;
        
    }
    
    private boolean CampoInvalido(String texto){
       boolean rta = false;
        if (texto == null || texto.trim().isEmpty()) {
            rta = true;
        } 
        return rta;
    }
